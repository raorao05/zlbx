    	   <h2 class="l_bxxy_tit">关于印发《互联网保险风险专项整治工作实施方案》的通知</h2>
	   <div class="w_bxxy_r_n1">
		   <span><b>来源：</b><a href="javascript:;" title="中国保监会">中国保监会</a></span>
		   <span><b>创建者：</b>中联保险</span>
		   <span><b>创建时间：</b>2016-10-21</span>
	   </div>
          <p style="text-align:center;">
	<span style="font-size:14px;font-family:SimSun;">保监发〔</span><span style="font-size:14px;font-family:SimSun;">2016</span><span style="font-size:14px;font-family:SimSun;">〕</span><span style="font-size:14px;font-family:SimSun;">31</span><span style="font-size:14px;font-family:SimSun;">号</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">各省、自治区、直辖市人民政府：</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">《互联网保险风险专项整治工作实施方案》已经国务院同意，现印发给你们，请认真贯彻执行。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">互联网保险风险专项整治工作实施方案</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">为贯彻落实党中央、国务院决策部署，推动互联网保险风险专项整治工作有序开展，根据《关于促进互联网金融健康发展的指导意见》和《互联网金融风险专项整治工作实施方案》，制定本方案。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><strong>一、工作目标和原则</strong></span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（一）工作目标。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">规范互联网保险经营模式，优化市场发展环境，完善监管制度规则，实现创新与防范风险并重，促进互联网保险健康可持续发展，切实发挥互联网保险在促进普惠金融发展、服务经济社会方面的独特优势。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（二）工作原则。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">突出重点，积极稳妥。坚持问题导向，集中力量对存在的风险进行整治，有效打击各类违法违规活动，同时广泛排查保险业互联网经营模式和保险产品，为完善监管奠定基础。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">分类施策，标本兼治。根据违法违规情节分类施策纠偏，讲究方式方法、力度节奏，妥善化解存量风险，有效控制增量风险。既要立足当前，切实防范化解风险，震慑违法违规行为；又要着眼长远，以专项整治为契机，建立健全长效监管机制。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">明确责任，加强协作。保监会各相关部门和各省级派出机构要高度重视此次专项整治工作，加强组织领导，落实主体责任，充分考虑互联网保险跨区域、跨领域的特点，完善工作机制，加强内外协调和上下联动。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><strong>二、整治重点和措施</strong></span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（一）互联网高现金价值业务。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">重点查处和纠正以下问题：保险公司通过互联网销售保险产品，进行不实描述、片面或夸大宣传过往业绩、违规承诺收益或者承担损失等误导性描述。具体措施：一是加强互联网保险信息披露监管，要求保险公司严格按照有关规定披露产品信息，满足消费者知情权。二是排查万能型人身保险产品（包括高现价产品）相关风险，出台有针对性的监管政策。三是加大互联网高现价业务查处力度，对于存在违规问题的公司予以严肃查处。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（二）保险机构依托互联网跨界开展业务。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">重点查处和纠正以下问题：一是保险公司与不具备经营资质的第三方网络平台合作开展互联网保险业务的行为。二是保险公司与存在提供增信服务、设立资金池、非法集资等行为的互联网信贷平台合作，引发风险向保险领域传递。三是保险公司在经营互联网信贷平台融资性保证保险业务过程中，存在风控手段不完善、内控管理不到位等情况。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（三）非法经营互联网保险业务。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">一是清理互联网保险经营资质，重点查处非持牌机构违规开展互联网保险业务，互联网企业未取得业务资质依托互联网开展保险业务等问题。二是查处不法机构和不法人员通过互联网利用保险公司名义或假借保险公司信用进行非法集资。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">保险机构不得与未取得相应业务资质的互联网金融从业机构开展合作，保险机构与互联网企业合作开展业务不得违反相关法律法规规定，不得通过互联网跨界开展金融活动实现监管套利。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（四）配套措施。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">互联网保险从业机构应严格落实客户资金第三方存管制度要求，保护客户资金安全。依靠举报和重罚机制及时发现问题，纠正不当行为，对违法违规机构进行严厉查处。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><strong>三、组织领导和职责分工</strong></span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（一）组织机构。保监会成立互联网保险专项整治工作领导小组，领导小组组长由保监会负责同志担任。设立领导小组办公室，负责组织协调相关工作。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（二）职责分工。保监会负责专项整治工作的总体部署和重大事项的协调处理，制定互联网保险风险专项整治工作实施方案，按进度推进专项整治工作，组织研究专项整治工作中遇到的新问题，做好专项整治工作总结汇报，汇总提出长效机制建设建议。在省级人民政府统一领导下，省金融办（局）与保监会省级派出机构共同牵头负责本地区分领域整治工作，共同承担分领域整治任务。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><strong>四、时间进度</strong></span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（一）摸底排查。通过全面排查、随机抽查等方式摸排风险底数，制定整改方案。对行业万能险存在的流动性风险、资产负债错配风险、利差损风险和销售误导风险等进行排查。此项工作于</span><span style="font-size:14px;font-family:SimSun;">2016</span><span style="font-size:14px;font-family:SimSun;">年</span><span style="font-size:14px;font-family:SimSun;">7</span><span style="font-size:14px;font-family:SimSun;">月底前完成。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（二）查处整改。严格按照制度规定，对相关问题限时、全面整改。适时下发专项通知，规范中短存续期产品。严肃查处，对于违规经营的市场主体采取叫停业务、责令整改等监管措施。此项工作于</span><span style="font-size:14px;font-family:SimSun;">2016</span><span style="font-size:14px;font-family:SimSun;">年</span><span style="font-size:14px;font-family:SimSun;">11</span><span style="font-size:14px;font-family:SimSun;">月底前完成。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（三）总结报告。按照《互联网金融风险专项整治工作实施方案》要求，认真总结此次专项整治工作，将有关情况汇总形成书面报告，报送互联网金融风险专项整治工作领导小组办公室。此项工作于</span><span style="font-size:14px;font-family:SimSun;">2017</span><span style="font-size:14px;font-family:SimSun;">年</span><span style="font-size:14px;font-family:SimSun;">1</span><span style="font-size:14px;font-family:SimSun;">月底前完成。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;"><strong>五、保障措施</strong></span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（一）加强组织领导。高度重视本次专项整治工作，深刻认识防范化解互联网金融风险的重要意义，按照工作方案的统一部署，细化整治内容，认真开展工作，狠抓落实，确保专项整治工作落到实处。</span>
</p>
<p>
	<span style="font-family:SimSun;font-size:14px;text-indent:21pt;line-height:1.5;">（二）务求工作实效。创新方式方法，确保专项整治工作不走过场。深入剖析问题成因，分类处理。坚持即查即改，对于需要协调解决的问题，要说明情况，并提出有针对性和可操作性的意见建议。</span>
</p>
<p style="text-indent:21.0pt;">
	<span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（三）加强协调配合。针对互联网金融保险活动跨区域、跨领域、难追责的特点，做好上下沟通及内外协调，形成风险防范合力。加强协作配合，明确职责与分工，推动各有关部门纵横联动和信息共享，确保专项整治工作顺利推进。</span><span></span>
</p>
<p>
	<span style="font-size:14px;font-family:SimSun;">（四）建立长效机制。以整治工作中发现的问题为导向，从完善制度、深化改革、加强监督入手，及时积累总结经验，加强对互联网保险的监测预警，引导保险机构加强风险管控，形成对互联网保险领域全覆盖的长效监管体制机制。</span><span></span>
</p>   </div>
