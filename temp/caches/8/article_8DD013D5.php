    	   <h2 class="l_bxxy_tit">“保险帮我解忧愁”</h2>
	   <div class="w_bxxy_r_n1">
		   <span><b>来源：</b><a href="javascript:;" title="中国保险报">中国保险报</a></span>
		   <span><b>创建者：</b>中联保险</span>
		   <span><b>创建时间：</b>2016-09-22</span>
	   </div>
          <p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	“保险是怎么买怎么对，钱又不多，咱给老人买个安心。”近日，记者在湖南省长沙市开福区四方坪街道办事处左岸社区采访时，49岁的童加莲大姐如是说。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	虽然自己的腿还没好利索，仍需借助一副拐杖才能走路，但童大姐还是第一个来到社区便民服务大厅，给自己73岁的老母亲刘荣华续缴保费。“去年10月份给我妈买了一份国寿的老年人意外保险，没想到10月底，老太太晚上起夜时就给摔伤了，整个腰椎都摔坏了，医保报销了1万多，剩下的保险公司又给报了4000多，这样一来我们做儿女的只要负责照看就行了。”童大姐说。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	在人口老龄化的大背景下，老年人意外事故频发，已严重影响到老人自己及家人的生活质量。对此，四方坪街道办事处副主任黄文革表示，在当前财政资金支持有限的情况下，合适的保险产品显得尤为重要。四方坪街道常住人口1万多人，老年人有近2000人，平时磕磕碰碰的意外基本上每周都有。一旦发生事故，特困家庭还可申请财政救助或慈善救助资金来缓解现实困境，但对于普通百姓而言，只能依靠家庭的自我救助。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	“有条件的老人还好，如果条件不是很好，儿女扯皮的现象时有发生，甚至还有闹到街道办来的，而有了保险，这个问题就好解决。归根结底一句话：保险帮我解忧愁！”黄文革说。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	实际上，老年人意外险仅仅是中国人寿湖南省分公司（以下简称“湖南国寿”）服务民生的一个缩影。近年来，该公司已先后开发出计生保险、村干部保险以及扶贫保险等80余款与民生息息相关的保险产品，全方位满足了湖南省各行各业、不同家庭及个体的保险保障需求。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	“关注民生、服务民生是我们的责任所在。”湖南国寿副总经理蔡郁青表示，近年来，湖南国寿按照“政府搭台、保险唱戏”的政保合作模式，借助国家有利政策与各级政府部门开展合作，大力推动各类民生保险业务发展，民生保险业务已成为湖南国寿团险业务大发展的最大增长极。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	如何准确地利用客户的碎片化时间、提供快捷高效的服务，也成为湖南国寿必须跨过的一道坎。在此背景下，e门店便应势而生了。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	“客户的需求在哪里、营销的痛点在哪里，我们的技术攻关就指向哪里。在总公司的集中部署下，我们开发了e门店这一与公司短险管理系统实时对接的互联网销售系统。目前，这一系统已成为公司短险销售核心平台，可全面满足实时出单及数据流转安全性等要求。现在，凡是互联网能到达的地方，e门店就能扎根，也让我们的出单时效达到了3-5秒/单，较好地满足了客户的投保需求和满意度。”湖南国寿信息部总经理文承标表示，目前，湖南国寿e门店在总部e门店体系基础上，结合本地短险业务销售情况，还创新研发了“湖南中介出单平台”“随行e门店”“微信e门店”等3个支持平台，基本实现了电子化服务对短险市场服务的全覆盖。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	“1996年我买过保险，手续办起来太麻烦了，所以后来就懒得买了，不过现在确实不一样了！”40多岁的韶山市清溪镇清溪村村民罗美芝，多年来一直在火车站附近的市场做服装生意，自家新建的两层小楼装修得温馨而舒适，在得知记者是来了解保险的有关情况时，一边说着自己对保险的看法，一边走到卧室拿出了一张小额保单。“您看看这个，100块钱一张的惠农保险，前两年村里就有人买，不过我嫌麻烦从来也没买过，今年晓红过来了，跟我说拿身份证一刷当场就能办好，结果还真是，没几分钟就弄好了。”罗美芝说。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	中国人寿韶山市分公司火车站营业部营销员张晓红跟罗美芝是多年的好朋友，每次想让罗大姐买保险时都被罗大姐嫌麻烦拒绝了。“她们家条件确实挺好的，不过现在生意难做，她都打算明年撤店呢，可俩闺女还在上学，真要有个什么意外，没个保险肯定不行，所以今年拿到这个e门店的机器后我马上就来找她了，不到十分钟给她们一家四口全办完了。”张晓红说。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	“现在，利用微信平台，突破传统保险销售的局限，让所有客户可随时、随地、随心购买短险产品，实现365天、24小时无盲点保险服务。”文承标说。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	在左岸社区办事员胡敏看来，这个系统确实极大地方便了她们的工作：“以前也经办过其他保险产品，时间会比较长。现在拿身份证一刷，保单马上就出来了，就算办理高峰期，社区居民也不会因排队而起纠纷了。”胡敏说。
</p>
<p style="margin:0px 3px 15px;padding:0px;text-indent:30px;font-family:宋体;font-size:14px;line-height:28px;white-space:normal;background-color:#FFFFFF;">
	“昨天居委会贴出通知，说今天开始卖（老年意外险）了，我们几个老姐妹遛弯回来就约着一块儿来买，省得给儿女添麻烦。再说现在也方便，交完钱拿了保单就能回家吃饭，啥事也不耽误。”采访结束后正准备离开左岸社区便民服务大厅时，记者迎面遇见的刘阿姨如是说。
</p>   </div>
