	<i></i><b>公司概况</b>Company Profile
</div>
<p>
	<span style="font-family:Microsoft YaHei;font-size:16px;">湖南中联保险经纪股份有限公司是2011年12月经中国保监会批准，由独立投资人创立，从事保险经纪业务的综合性保险经纪公司。总部设在湖南长沙，公司注册资金为1400万元。</span><br />
<span style="font-family:Microsoft YaHei;font-size:16px;">公司服务范围包括为投保人拟定投保方案、选择投保人、办理投保手续；协助被保险人或受益人进行索赔；再保险经纪服务；为委托人提供防灾、防损或风险评估、提供包括各类财产保险、人身保险、信用和责任保险、农业保险及再保险方面的专业风险管理咨询和保险经纪服务。业务涉足险种有财产险、建筑工程险、安装工程险、机动车辆险、各种责任险、货运险、船舶险、意外险、健康医疗险、养老险、海洋石油险类等险种。</span> 
</p>
<p>
	<span style="font-family:Microsoft YaHei;font-size:16px;">中联保险经纪自成立以来，以“维护投保人（被保险人）的合法权益”为使命，致力于建设“专业一流、服务优质、客户信任、创造价值”的国内知名的保险中介集团。公司经过四年的发展，规模不断扩大，截至2016年6月，旗下已设立6个营业部，具备完善的服务网络。公司拥有一批专业技术队伍，具有深厚的专业基础和丰富的实践经验，拥有大批资深而优秀的业务精英，具有丰富的风险管理顾问、保险项目招投标、承保和理赔安排经验，为不同行业的企事业单位及个人设计专业化、个性化的保险方案，并提供全面、一站式的售后服务。公司现为湖南省保险中介行业副会长兼保险经纪专业委员会主任单位。</span> 
</p>
