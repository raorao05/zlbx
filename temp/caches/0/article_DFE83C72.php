    	   <h2 class="l_bxxy_tit">中国家庭财富增速居亚洲之首 逾4成家庭关注养老保障</h2>
	   <div class="w_bxxy_r_n1">
		   <span><b>来源：</b><a href="javascript:;" title="南方日报">南方日报</a></span>
		   <span><b>创建者：</b>中联保险</span>
		   <span><b>创建时间：</b>2016-10-21</span>
	   </div>
          <p class="MsoNormal" style="text-indent:21.0pt;">
	<span style="font-size:14px;font-family:SimSun;line-height:2;">安联集团</span><span style="font-size:14px;font-family:SimSun;line-height:2;">22</span><span style="font-size:14px;font-family:SimSun;line-height:2;">日公布了最新《安联全球财富报告》，对</span><span style="font-size:14px;font-family:SimSun;line-height:2;">53</span><span style="font-size:14px;font-family:SimSun;line-height:2;">个国家和地区的家庭资产和债务情况进行了剖析。这是安联连续第七年发布全球财富报告。根据报告的调查结果，全球金融资产在</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2015</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年增长了</span><span style="font-size:14px;font-family:SimSun;line-height:2;">4.9%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">，仅略高于经济活动的增速。而在此前的三年中，金融资产的平均增速达到</span><span style="font-size:14px;font-family:SimSun;line-height:2;">9%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:SimSun;line-height:2;">&nbsp; “金融资产的发展已经到了一个重要关头”，安联</span><span style="font-size:14px;font-family:SimSun;line-height:2;">首席经济学家迈尔克海瑟（</span><span style="font-size:14px;font-family:SimSun;line-height:2;">Michael Heise</span><span style="font-size:14px;font-family:SimSun;line-height:2;">）说。“很显然，极端的货币政策正在失去影响，即使在资产价格</span><span style="font-size:14px;font-family:SimSun;line-height:2;">上也是如此。因此，资产增长的重要驱动力已不复存在。与此同时，利率持续走低，甚至跌入负值。对于储户来说，前景不容乐观。”</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:SimSun;line-height:2;">&nbsp; &nbsp;值得一提的是，</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2015</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年中国家庭的金融资产增速达到</span><span style="font-size:14px;font-family:SimSun;line-height:2;">18.3%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">，高居亚洲首位，这一增速略低于过去十年</span><span style="font-size:14px;font-family:SimSun;line-height:2;">20.4%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">的平均值。这些惊人的增长率反映的不仅是中国经济的潜力，更是中国依旧存在的巨大潜在需求</span><span style="font-size:14px;font-family:SimSun;line-height:2;">。截至</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2015</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年底，中国家庭的金融资产总额为</span><span style="font-size:14px;font-family:SimSun;line-height:2;">19.7</span><span style="font-size:14px;font-family:SimSun;line-height:2;">万亿欧元，占亚洲地区的</span><span style="font-size:14px;font-family:SimSun;line-height:2;">46%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">。</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2015</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年，中国的人均金融资产净额达到</span><span style="font-size:14px;font-family:SimSun;line-height:2;">11500</span><span style="font-size:14px;font-family:SimSun;line-height:2;">欧元，较</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2014</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年</span><span style="font-size:14px;font-family:SimSun;line-height:2;">7990</span><span style="font-size:14px;font-family:SimSun;line-height:2;">欧元增长了近</span><span style="font-size:14px;font-family:SimSun;line-height:2;">44%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">，处于亚洲第六，在《安联全球财富报告》分析的</span><span style="font-size:14px;font-family:SimSun;line-height:2;">53</span><span style="font-size:14px;font-family:SimSun;line-height:2;">个国家和地区列</span><span style="font-size:14px;font-family:SimSun;line-height:2;">28</span><span style="font-size:14px;font-family:SimSun;line-height:2;">位，较</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2014</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年上升</span><span style="font-size:14px;font-family:SimSun;line-height:2;">5</span><span style="font-size:14px;font-family:SimSun;line-height:2;">位。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:SimSun;line-height:2;">&nbsp; &nbsp;在家庭负债方面，中国家庭贷款继</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2014</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年增长</span><span style="font-size:14px;font-family:SimSun;line-height:2;">16.6%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">之后，在</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2015</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年增加了</span><span style="font-size:14px;font-family:SimSun;line-height:2;">16.8%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">。《安联全球财富报告》指出，相较于发达国家，中国的家庭总债务仍然相当低，其贷款总额在</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2015</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年底仅占国内生产总值（</span><span style="font-size:14px;font-family:SimSun;line-height:2;">GDP</span><span style="font-size:14px;font-family:SimSun;line-height:2;">）的</span><span style="font-size:14px;font-family:SimSun;line-height:2;">39.1%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">。</span><span style="font-size:14px;font-family:SimSun;line-height:2;">2015</span><span style="font-size:14px;font-family:SimSun;line-height:2;">年，中国家庭金融资产净额增长了</span><span style="font-size:14px;font-family:SimSun;line-height:2;">18.7%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">。</span><span></span>
</p>
<p class="MsoNormal">
	<span><span style="font-size:14px;font-family:SimSun;line-height:2;">&nbsp; &nbsp;中德安联</span></span><span style="font-size:14px;font-family:SimSun;line-height:2;">人寿保险</span><span style="font-size:14px;font-family:SimSun;line-height:2;">有限公司首席执行官陈良</span><span style="font-size:14px;font-family:SimSun;line-height:2;">表示，“随着金融资产稳步增长和家庭债务的增加，中国家庭在理财</span><span style="font-size:14px;font-family:SimSun;line-height:2;">规划中对保险的关注度显著提升，特别关注养老及因重疾引发的对未来家庭生活品质、对子女成长教育以及对赡养父母的长期影响。年金与健康保障类产品持续获得高端人群的青睐。”</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:SimSun;line-height:2;">&nbsp; &nbsp;《安联全球财富报告》还显示，中国家庭的分散投资进程正在继续，家庭金融资产投资已越来越注重绩效，把资产从低收益的储蓄账户转向高收益的银行和理财产品。</span><span style="font-size:14px;font-family:SimSun;line-height:2;">44%</span><span style="font-size:14px;font-family:SimSun;line-height:2;">的中国家庭将未来养老保障作为家庭理财投资的主要驱动因素之一。</span><span></span>
</p>
